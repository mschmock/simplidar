    // fill image white
    private static void setPixels() {
        int sizeX = DataManager.getClusterSizeX();
        int sizeY = DataManager.getClusterSizeY();
        //Pixel einf√§rben
        for (int i = 0; i < sizeX; i++) {
            for (int j = 0; j < sizeY; j++) {
                image.setRGB(i, j, Color.white.getRGB());
            }
        }
    }

